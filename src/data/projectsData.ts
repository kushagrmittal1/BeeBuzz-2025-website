// Helper to extract YouTube video ID from URL
export function getYouTubeVideoId(url: string): string | null {
  // Handles both normal and share URLs
  const regExp =
    /(?:youtube\.com\/(?:watch\?v=|embed\/|v\/|shorts\/)|youtu\.be\/)([A-Za-z0-9_-]{11})/;
  const match = url.match(regExp);
  return match ? match[1] : null;
}

// Helper to detect if a video is vertical (YouTube Shorts or vertical format)
export function detectVideoOrientation(url: string): "horizontal" | "vertical" {
  // Check if it's a YouTube Shorts URL
  if (url.includes("/shorts/")) {
    return "vertical";
  }

  // For now, we'll assume most regular YouTube videos are horizontal
  // In a real implementation, you might want to fetch video metadata to determine actual aspect ratio
  return "horizontal";
}

