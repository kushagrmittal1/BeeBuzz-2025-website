// Function to fetch video details from YouTube oEmbed with timeout
export const fetchVideoDetails = async (url: string): Promise<Video | null> => {
  const videoId = getYouTubeVideoId(url);
  if (!videoId) return null;

  try {
    // oEmbed endpoint (no API key required) with timeout
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 5000); // 5 second timeout
    
    const oembedUrl = `https://www.youtube.com/oembed?url=${encodeURIComponent(
      url
    )}&format=json`;
    const res = await fetch(oembedUrl, { 
      signal: controller.signal,
      headers: {
        'Accept': 'application/json',
      }
    });
    
    clearTimeout(timeoutId);
    
    if (!res.ok) throw new Error("oEmbed fetch failed");
    const data = await res.json();
    return {
      id: videoId,
      title: data.title,
      thumbnail: data.thumbnail_url,
      url,
    };
  } catch (e) {
    // fallback: use thumbnail from videoId, generic title
    return {
      id: videoId,
      title: "YouTube Video",
      thumbnail: `https://img.youtube.com/vi/${videoId}/hqdefault.jpg`,
      url,
    };
  }
};

